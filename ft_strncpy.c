#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t		counter;

	counter = 0;
	while (src[counter] != '\0' && counter < n)
	{
		dest[counter] = src[counter];
		counter++;
	}
	while (counter < n)
	{
		dest[counter] = '\0';
		counter++;
	}
	return (dest);
}
